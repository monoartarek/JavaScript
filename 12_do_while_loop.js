/*
Great! Now let’s learn the do...while loop in JavaScript — 
it's similar to the while loop, but with one big difference:

✅ It runs the code at least once, even if the condition is false.

do {
  // code block which is run first instead check the condition
} while (condition);


*/


let i = 1;

do {
  console.log(i);
  i++;
} while (i <= 5);
